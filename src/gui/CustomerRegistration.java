    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 1) {

            int selectedRow = jTable1.getSelectedRow();

            String status = jTable1.getValueAt(selectedRow, 6).toString();

            if (status.equals("Deactive")) {
                JOptionPane.showMessageDialog(this, "This car already deleted", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                jTextField1.setText(jTable1.getValueAt(selectedRow, 1).toString());
                jTextField2.setText(jTable1.getValueAt(selectedRow, 2).toString());
                jTextField4.setText(jTable1.getValueAt(selectedRow, 3).toString());
                jTextField6.setText(jTable1.getValueAt(selectedRow, 4).toString());
                jTextField3.setText(jTable1.getValueAt(selectedRow, 5).toString());

                jButton1.setText("Update");
                jButton2.setVisible(true);
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jTextField4.setEnabled(false);
            }

        } else if (evt.getClickCount() == 2) {

            int selectedRow = jTable1.getSelectedRow();

            String status = jTable1.getValueAt(selectedRow, 6).toString();

            if (status.equals("Deactive")) {
                JOptionPane.showMessageDialog(this, "This car already deleted", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                int option = JOptionPane.showConfirmDialog(this, "Do you want to Delete this customer?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) { //0 = Yes

                    String id = jTable1.getValueAt(selectedRow, 0).toString();

                    MySQL.iud("UPDATE `customer` SET `status_id`='2' WHERE `id`='" + id + "' ");
                }

                reset();
                jButton1.setText("Register");
                jButton2.setVisible(false);
                jTextField1.setEnabled(true);
                jTextField2.setEnabled(true);
                jTextField4.setEnabled(true);
                loadCustomer();
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

