    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        String price = jTextField1.getText();
        String text = price + evt.getKeyChar();

        if (!Pattern.compile("0|0[.]|0[.][0-9]*|[1-9]|[1-9][0-9]*|[1-9][0-9]*[.]|[1-9][0-9]*[.][0-9]*").matcher(text).matches()) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField1KeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String sname = jTextField2.getText();
        String price = jTextField1.getText();

        if (sname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter service name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter service price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                ResultSet rs = MySQL.search("SELECT * FROM `service` WHERE `name`='" + sname + "' AND `status_id`='1' ");

                if (jButton1.getText().equals("Register")) { //register

                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "This service already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {

                        MySQL.iud("INSERT INTO `service`(`name`,`price`,`status_id`) VALUES ('" + sname + "','" + price + "','1') ");
                    }
                } else {  //update
                    int selectedRow = jTable1.getSelectedRow();
                    String id = jTable1.getValueAt(selectedRow, 0).toString();

                    MySQL.iud("UPDATE `service` SET `price`='" + price + "' WHERE `id`='" + id + "' ");

                    jButton1.setText("Register");
                    jButton2.setVisible(false);

                }

                reset();
                jTextField2.grabFocus();
                loadServices();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

