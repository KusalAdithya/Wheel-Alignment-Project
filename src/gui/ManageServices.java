    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 1) {

            int selectedRow = jTable1.getSelectedRow();

            String status = jTable1.getValueAt(selectedRow, 3).toString();

            if (status.equals("Deactive")) {

                int option = JOptionPane.showConfirmDialog(this, "Do you want to Active this service?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) { //0 = Yes

                    String id = jTable1.getValueAt(selectedRow, 0).toString();

                    MySQL.iud("UPDATE `service` SET `status_id`='1' WHERE `id`='" + id + "' ");
                }
                reset();
                jButton1.setText("Register");
                jButton2.setVisible(false);
                jTextField2.grabFocus();
                loadServices();
                
            } else {
                jTextField2.setText(jTable1.getValueAt(selectedRow, 1).toString());
                jTextField1.setText(jTable1.getValueAt(selectedRow, 2).toString());

                jButton1.setText("Update");
                jButton2.setVisible(true);
            }

        } else if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();

            String status = jTable1.getValueAt(selectedRow, 3).toString();
            if (status.equals("Deactive")) {
                JOptionPane.showMessageDialog(this, "This service already deleted", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                int option = JOptionPane.showConfirmDialog(this, "Do you want to Delete this service?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) { //0 = Yes

                    String id = jTable1.getValueAt(selectedRow, 0).toString();

                    MySQL.iud("UPDATE `service` SET `status_id`='2' WHERE `id`='" + id + "' ");
                }

                reset();
                jButton1.setText("Register");
                jButton2.setVisible(false);
                jTextField2.grabFocus();
                loadServices();
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

