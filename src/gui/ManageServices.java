    public void loadServices() {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `service` INNER JOIN `status` ON `service`.`status_id`=`status`.`id` ORDER BY  `service`.`id` ASC ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("service.id"));
                v.add(rs.getString("service.name"));
                v.add(rs.getString("service.price"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadServices(String search) {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `service`  INNER JOIN `status` ON `service`.`status_id`=`status`.`id` WHERE  `service`.`name` LIKE '%" + search + "%' ORDER BY  `service`.`id` ASC ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("service.id"));
                v.add(rs.getString("service.name"));
                v.add(rs.getString("service.price"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

