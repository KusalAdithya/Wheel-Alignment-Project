    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String Pid = jTextField2.getText();
        String Pname = jTextField1.getText();
        String category = jComboBox1.getSelectedItem().toString();
        String brand = jComboBox2.getSelectedItem().toString();

        if (Pid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter product id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[0-9]*").matcher(Pid).matches()) {
            JOptionPane.showMessageDialog(this, "Please enter valid product id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Pname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter product name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (category == "Select") {
            JOptionPane.showMessageDialog(this, "Please select a category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (brand == "Select") {
            JOptionPane.showMessageDialog(this, "Please select a brand", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `product` WHERE `id`='" + Pid + "' ");

                if (jButton1.getText().equals("Register")) { //register

                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "This product already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        ResultSet rs1 = MySQL.search("SELECT `id` FROM `brand` WHERE `name`='" + brand + "' ");
                        rs1.next();

                        ResultSet rs2 = MySQL.search("SELECT `id` FROM `category` WHERE `name`='" + category + "' ");
                        rs2.next();

                        String brand_id = rs1.getString("id");
                        String category_id = rs2.getString("id");

                        MySQL.iud("INSERT INTO `product`(`id`,`name`,`category_id`,`brand_id`,`status_id`) VALUES ('" + Pid + "','" + Pname + "','" + category_id + "','" + brand_id + "','1') ");

                    }

                } else {  //update
                    MySQL.iud("UPDATE `product` SET `name`='" + Pname + "' WHERE `id`='" + Pid + "' ");
                }

                jButton1.setText("Register");
                jButton2.setVisible(false);
                jTextField1.setEnabled(true);
                jTextField2.setEnabled(true);

                reset();
                loadProducts();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

