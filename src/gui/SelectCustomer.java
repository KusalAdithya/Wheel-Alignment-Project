    public void loadCustomer() {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `customer` INNER JOIN `status` ON `customer`.`status_id`=`status`.`id` WHERE `customer`.`status_id`='1' ORDER BY `customer`.`id` ASC ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("customer.id"));
                v.add(rs.getString("customer.car_number"));
                v.add(rs.getString("customer.fname"));
                v.add(rs.getString("customer.lname"));
                v.add(rs.getString("customer.m_number"));
                v.add(rs.getString("customer.email"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadCustomer(String search) {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `customer` INNER JOIN `status` ON `customer`.`status_id`=`status`.`id` WHERE `customer`.`car_number` LIKE '%" + search + "%' AND `customer`.`status_id`='1'  ORDER BY `customer`.`id` ASC ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("customer.id"));
                v.add(rs.getString("customer.car_number"));
                v.add(rs.getString("customer.fname"));
                v.add(rs.getString("customer.lname"));
                v.add(rs.getString("customer.m_number"));
                v.add(rs.getString("customer.email"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

