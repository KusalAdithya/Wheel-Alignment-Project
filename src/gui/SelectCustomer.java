    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Customer", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                if (in != null) {
                    String cid = jTable1.getValueAt(r, 0).toString();
                    String carno = jTable1.getValueAt(r, 1).toString();
                    String cmnumber = jTable1.getValueAt(r, 4).toString();
                    String cemail = jTable1.getValueAt(r, 5).toString();

                    in.jLabel7.setText(cid);
                    in.jLabel8.setText(carno);
                    in.jLabel10.setText(cmnumber);
                    in.jLabel12.setText(cemail);

                    in.jButton5.setText("Update Customer");
                    in.jButton5.setEnabled(false);

                    this.dispose();
                }

            }
        }


    }//GEN-LAST:event_jTable1MouseClicked

