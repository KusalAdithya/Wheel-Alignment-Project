    public void loadSupplier() {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `supplier` INNER JOIN `company` ON `supplier`.`company_id`=`company`.`id` INNER JOIN `status` ON `supplier`.`status_id`=`status`.`id` ORDER BY `supplier`.`id` DESC ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("supplier.id"));
                v.add(rs.getString("supplier.fname"));
                v.add(rs.getString("supplier.lname"));
                v.add(rs.getString("supplier.number"));
                v.add(rs.getString("supplier.email"));
                v.add(rs.getString("company.name"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadSupplier(String search) {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `supplier` INNER JOIN `company` ON `supplier`.`company_id`=`company`.`id` INNER JOIN `status` ON `supplier`.`status_id`=`status`.`id` WHERE `supplier`.`fname` LIKE '" + search + "%' OR `supplier`.`lname` LIKE '" + search + "%'  OR `supplier`.`number` LIKE '" + search + "%'  ORDER BY `supplier`.`id` ASC ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("supplier.id"));
                v.add(rs.getString("supplier.fname"));
                v.add(rs.getString("supplier.lname"));
                v.add(rs.getString("supplier.number"));
                v.add(rs.getString("supplier.email"));
                v.add(rs.getString("company.name"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

