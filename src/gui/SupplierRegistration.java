    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String fname = jTextField2.getText();
        String lname = jTextField4.getText();
        String number = jTextField6.getText();
        String email = jTextField3.getText().toLowerCase();
        String company = jComboBox1.getSelectedItem().toString();

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter first name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter last name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (number.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("0[0-9]{9}").matcher(number).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid contact number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("^[a-z0-9._%+-]+@[a-z0-9.-]+\\.[a-z]{2,6}$").matcher(email).matches()) {
            JOptionPane.showMessageDialog(this, "Please enter valid email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (company == "Select") {
            JOptionPane.showMessageDialog(this, "Please select a company", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `supplier` WHERE`email`='" + email + "' OR `number`='" + number + "' ");

                if (jButton1.getText().equals("Register")) { //register 

                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "This supplier already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        ResultSet rs1 = MySQL.search("SELECT `id` FROM `company` WHERE `name`='" + company + "' ");
                        rs1.next();

                        String cid = rs1.getString("id");

                        MySQL.iud("INSERT INTO `supplier`(`fname`,`lname`,`email`,`number`,`company_id`,`status_id`) VALUES ('" + fname + "','" + lname + "','" + email + "','" + number + "','" + cid + "','1') ");
                    }

                } else {  //update
                    int selectedRow = jTable1.getSelectedRow();
                    String sid = jTable1.getValueAt(selectedRow, 0).toString();

                    MySQL.iud("UPDATE `supplier` SET `number`='" + number + "' , `email`='" + email + "' WHERE `id`='" + sid + "' ");

                    jButton1.setText("Register");
                    jButton2.setVisible(false);
                    jTextField2.setEnabled(true);
                    jTextField4.setEnabled(true);
                    jComboBox1.setEnabled(true);
                }

                reset();
                loadSupplier();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

